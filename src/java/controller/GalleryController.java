    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //        processRequest(request, response);

        int galleryID = Integer.parseInt(request.getParameter("id"));
        ImageDAO imageDAO = new ImageDAO();
        GalleryDAO galleryDAO = new GalleryDAO();
        galleryDAO.increaseGalleryViewByID(galleryID);
        Gallery gallery = galleryDAO.getGalleryWithViewsByID(galleryID);

        String hits = String.format("%06d", gallery.getGalleryViews() + 1);
        ArrayList<Image> listImg = imageDAO.getListImageByGalleryID(galleryID);
        
        request.setAttribute("pageId", gallery.getGalleryID());
        request.setAttribute("gallery", gallery);
        request.setAttribute("hitsChar", hits.toCharArray());
        request.setAttribute("listImg", listImg);
        request.getRequestDispatcher("gallery.jsp").forward(request, response);
    }

